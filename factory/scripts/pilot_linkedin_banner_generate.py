def main() -> int:
    ap = argparse.ArgumentParser(description="Pilot LinkedIn banner (linkedin-studio soul)")
    ap.add_argument("--client", required=True, choices=sorted(CLIENTS))
    ap.add_argument("--model", default="anthropic/claude-opus-4.8")
    ap.add_argument("--out", default=None)
    ap.add_argument("--max-tokens", type=int, default=50000)
    ap.add_argument("--temperature", type=float, default=0.6)
    ap.add_argument("--timeout", type=int, default=700)
    a = ap.parse_args()

    spec = CLIENTS[a.client]
    out_dir = Path(a.out) if a.out else (
        ROOT / "clients" / spec["slug"] / "04-production" / "pilot-linkedin-banner"
    )
    out_dir.mkdir(parents=True, exist_ok=True)

    load_env()
    system = build_system()
    payload = {
        "model": a.model,
        "messages": [
            {"role": "system", "content": system},
            {"role": "user", "content": spec["user"]},
        ],
        "max_tokens": a.max_tokens,
        "temperature": a.temperature,
        "reasoning": {"max_tokens": 1500},
    }
    req = urllib.request.Request(
        f"{API_BASE}/chat/completions", data=json.dumps(payload).encode(),
        headers={
            "Authorization": f"Bearer {or_key()}",
            "Content-Type": "application/json",
            "HTTP-Referer": "https://design-hermes.local/",
            "X-Title": spec["x_title"],
        },
        method="POST",
    )
    print(f"building {spec['title']} with {a.model} ...", flush=True)
    started = time.time()
    try:
        with urllib.request.urlopen(req, timeout=a.timeout) as resp:
            data = json.loads(resp.read().decode())
    except urllib.error.HTTPError as e:
        raise SystemExit(f"HTTP {e.code}: {e.read().decode(errors='ignore')[:400]}")
    choice = (data.get("choices") or [{}])[0]
    raw = (choice.get("message") or {}).get("content") or ""
    html = extract_html(raw)
    (out_dir / "banner.html").write_text(html, encoding="utf-8")
    (out_dir / "raw.txt").write_text(raw, encoding="utf-8")

    usage = data.get("usage") or {}
    cost = usage.get("cost")
    meta = {
        "client": a.client,
        "slug": spec["slug"],
        "model": a.model,
        "seconds": round(time.time() - started, 1),
        "finish_reason": choice.get("finish_reason"),
        "usage": usage,
        "cost_usd": cost,
        "html_bytes": len(html.encode()),
        "complete": html.lower().rstrip().endswith("</html>"),
        "out": str(out_dir / "banner.html"),
    }
    (out_dir / "meta.json").write_text(json.dumps(meta, indent=2))
    print(json.dumps({k: v for k, v in meta.items() if k != "usage"}, indent=2))
    return 0 if meta["complete"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
